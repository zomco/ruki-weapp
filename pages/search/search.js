const charsAlphabetCount = function(chars) {
  let count = 0;
  const sequences = chars.substr(2);
  const pattern = new RegExp('[A-Z]');
  for (let i = 0; i < sequences.length; i++) {
    if (pattern.test(sequences[i])) {
      count++;
    }
  }
  return count;
};

